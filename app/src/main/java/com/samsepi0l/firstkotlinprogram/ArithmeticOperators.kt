package com.samsepi0l.firstkotlinprogram

fun main() {
  val num1 = 8.2;
  val num2 = 4.2;

  var result: Double = num1 + num2;
  println("num1 + num2 = $result")

  result = num1 - num2;
  println("num1 - num2 = $result")

  result = num1 * num2;
  println("num1 * num2 = $result")

  result = num1 / num2;
  println("num1 / num2 = $result")

  result = num1 % num2
  println("num1 % num2 = $result ")
}